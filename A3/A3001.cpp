#include <stdio.h>

// สร้างอาร์เรย์เก็บข้อมูลของแท่งไม้แต่ละแท่ง (รองรับสูงสุด 1000 แท่งตามโจทย์)
int A[1005], B[1005];
long long L[1005], R[1005];

long long max_X = 0;  // เก็บน้ำหนักรวมเป้าหมายของโมบาย (หลังสมดุล)
long long sum_W = 0;  // เก็บน้ำหนักรวมเริ่มต้นของตุ้มน้ำหนักทั้งหมด

// ฟังก์ชัน DFS เพื่อท่องต้นไม้และคำนวณน้ำหนัก
void dfs(int u, int depth) {
    // ตรวจสอบฝั่งซ้าย
    if (A[u] == 1) {
        // หากเชื่อมกับตุ้มน้ำหนัก
        sum_W += L[u];
        
        long long req = 0;
        if (depth < 60) { 
            // คำนวณ น้ำหนักตุ้ม * (2 ^ ความลึก) โดยใช้ Bitwise Shift
            req = L[u] << depth; 
        } else {
            // กันกรณี overflow (แม้ตามข้อจำกัดของโจทย์จะไม่ถึงก็ตาม)
            req = 2000000000000000000LL; 
        }
        
        // อัปเดตค่าน้ำหนักรวมของโมบายที่มากที่สุดที่จำเป็น
        if (req > max_X) max_X = req;
    } else {
        // หากเชื่อมกับแท่งไม้อื่น ให้ค้นหาลงไปต่อ พร้อมเพิ่มความลึก
        dfs((int)L[u], depth + 1);
    }

    // ตรวจสอบฝั่งขวา (ทำเหมือนฝั่งซ้าย)
    if (B[u] == 1) {
        sum_W += R[u];
        long long req = 0;
        if (depth < 60) {
            req = R[u] << depth;
        } else {
            req = 2000000000000000000LL;
        }
        
        if (req > max_X) max_X = req;
    } else {
        dfs((int)R[u], depth + 1);
    }
}

int main() {
    int N;
    
    // รับจำนวนแท่งไม้
    if (scanf("%d", &N) != 1) return 0;
    
    // รับข้อมูลการเชื่อมต่อของแท่งไม้ตั้งแต่แท่งที่ 1 ถึง N
    for (int i = 1; i <= N; i++) {
        scanf("%d %lld %d %lld", &A[i], &L[i], &B[i], &R[i]);
    }
    
    // เริ่มต้นค้นหาจากแท่งไม้ที่ 1 (ความลึก = 1)
    dfs(1, 1);
    
    // ผลลัพธ์คือน้ำหนักที่ต้องเพิ่ม (น้ำหนักรวมที่ต้องใช้เป้าหมาย ลบด้วย น้ำหนักที่มีอยู่แล้ว)
    long long added_weight = max_X - sum_W;
    
    // แสดงผลลัพธ์
    printf("%lld\n", added_weight);
    
    return 0;
}