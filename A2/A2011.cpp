#include <stdio.h>

int main() {
    int num;
    int seen[10];         // 'กระดาษทด' สำหรับเก็บตัวเลขที่เคยเจอแล้ว
    int unique_count = 0; // ตัวแปรนับว่าตอนนี้เราจดเลขลงกระดาษทดไปกี่ตัวแล้ว

    // 1. วนลูปรับตัวเลข بالضبط 10 จำนวน
    for (int i = 0; i < 10; i++) {
        if (scanf("%d", &num) != 1) break; // รับค่าตัวเลขทีละตัว

        int is_duplicate = 0; // ตัวแปรสถานะ: 0 = ไม่ซ้ำ, 1 = ซ้ำ

        // 2. ค้นหาใน 'กระดาษทด' ว่าเคยมีเลขนี้หรือยัง
        for (int j = 0; j < unique_count; j++) {
            if (seen[j] == num) {
                is_duplicate = 1; // โป๊ะเชะ! เคยเจอแล้ว
                break;            // หยุดค้นหาทันที ประหยัดเวลา
            }
        }

        // 3. ถ้าเช็คแล้ว "ไม่ซ้ำ" (ไม่เคยเจอมาก่อน)
        if (is_duplicate == 0) {
            printf("%d ", num);       // พิมพ์เลขนั้นออกมา (เว้นวรรคด้วยตามโจทย์)
            seen[unique_count] = num; // บันทึกเลขนี้ลง 'กระดาษทด'
            unique_count++;           // เพิ่มจำนวนเลขที่เคยเจอขึ้นอีก 1
        }
    }

    printf("\n"); 
    
    return 0;
}