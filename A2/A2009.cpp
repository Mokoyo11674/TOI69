#include <stdio.h>
#include <stdlib.h>
#include <stdbool.h>

int M[1005][1005]; // ตารางเก็บผลการแข่งขัน (รองรับได้ถึง 1000 ทีม)
int teams[1005];   // อาร์เรย์เก็บทีมที่ยังมีชีวิตอยู่ในรอบปัจจุบัน
int next_round[1005]; // อาร์เรย์เก็บทีมที่ผ่านเข้ารอบไปรอในรอบถัดไป

int main() {
    int n;
    int special_team = 0; 
    
    // 1. รับจำนวนทีม (N) และ หมายเลขทีมที่มีบัตรพิเศษ (ถ้ามี)
    // ใช้เทคนิคการอ่านบรรทัดแรก เผื่อโจทย์บางเคสไม่มีบัตรพิเศษให้
    char line[256];
    if (fgets(line, sizeof(line), stdin) != NULL) {
        // ถ้าอ่านได้ 1 ค่า แปลว่ามีแค่ N ถ้าอ่านได้ 2 ค่า แปลว่ามีบัตรพิเศษ (C) ด้วย
        sscanf(line, "%d %d", &n, &special_team);
    }

    // 2. รับตารางผลการแข่งขัน M[i][j]
    for (int i = 1; i <= n; i++) {
        for (int j = 1; j <= n; j++) {
            scanf("%d", &M[i][j]);
        }
    }

    // 3. เตรียมผู้เข้าแข่งขันรอบแรก (ทีม 1 ถึง N)
    for (int i = 0; i < n; i++) {
        teams[i] = i + 1;
    }

    int current_teams = n; // จำนวนทีมที่เหลือรอดในขณะนี้
    bool card_used = false; // สถานะว่าบัตรพิเศษถูกใช้ไปหรือยัง (ใช้ได้แค่ครั้งเดียว)

    // 4. จำลองการแข่งขันไปเรื่อยๆ จนกว่าจะเหลือแชมป์เปี้ยนเพียง 1 ทีม
    while (current_teams > 1) {
        int match_idx = 0;

        // จับคู่แข่งทีละ 2 ทีม (0vs1, 2vs3, 4vs5 ...)
        for (int i = 0; i < current_teams; i += 2) {
            int t1 = teams[i];
            int t2 = teams[i + 1];
            
            // ดูผลล่วงหน้าจากตารางว่าใครควรจะชนะ
            int winner = M[t1][t2];

            // ตรวจสอบเงื่อนไข "บัตรพิเศษ"
            // ถ้าทีมที่มีบัตรพิเศษกำลังแข่ง และทำท่าว่าจะแพ้ (winner ไม่ใช่ตัวเอง)
            if ((t1 == special_team || t2 == special_team) && winner != special_team) {
                // ถ้าบัตรยังไม่เคยถูกใช้ ก็งัดออกมาใช้พลิกชนะเลย!
                if (!card_used) {
                    winner = special_team;
                    card_used = true; // บันทึกว่าบัตรถูกใช้ไปแล้ว
                }
            }

            // นำผู้ชนะใส่ในอาร์เรย์สำหรับรอบถัดไป
            next_round[match_idx++] = winner;
        }

        // อัปเดตรายชื่อทีมสำหรับรอบถัดไป
        for (int i = 0; i < match_idx; i++) {
            teams[i] = next_round[i];
        }
        
        // จำนวนทีมจะลดลงครึ่งหนึ่งในรอบถัดไป
        current_teams /= 2;
    }

    // 5. แสดงผลลัพธ์ (ทีมที่เหลือรอดทีมสุดท้ายคือแชมป์!)
    printf("%d\n", teams[0]);

    return 0;
}