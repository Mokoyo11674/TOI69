#include <stdio.h>

int main() {
    int n;
    
    // 1. รับจำนวนต้นไม้ N
    if (scanf("%d", &n) != 1) return 0;

    // จองพื้นที่ Array สำหรับเก็บความสูงของต้นไม้
    // โจทย์บอกว่า N ไม่เกิน 1,000 เราเผื่อเป็น 1,005 ไปเลยเพื่อความปลอดภัย
    int h[1005]; 

    // 2. รับความสูงของต้นไม้แต่ละต้นมาเก็บไว้ใน Array
    for (int i = 0; i < n; i++) {
        scanf("%d", &h[i]);
    }

    int count = 0; // ตัวแปรสำหรับนับจำนวนต้นไม้ที่นกชอบ

    // 3. วนลูปตรวจสอบต้นไม้ทีละต้น
    for (int i = 0; i < n; i++) {
        int left_ok = 0;  // สถานะฝั่งซ้าย (0 = ไม่รอด, 1 = รอด)
        int right_ok = 0; // สถานะฝั่งขวา (0 = ไม่รอด, 1 = รอด)

        // เช็คฝั่งซ้าย: ถ้านี่คือต้นแรกสุด (i == 0) ถือว่าฝั่งซ้ายไม่มีใครสูงกว่า (รอดเลย)
        // หรือ ถ้ามันสูงกว่าต้นฝั่งซ้าย (h[i] > h[i-1]) ก็ถือว่ารอด
        if (i == 0 || h[i] > h[i-1]) {
            left_ok = 1;
        }

        // เช็คฝั่งขวา: ถ้านี่คือต้นสุดท้าย (i == n-1) ถือว่าฝั่งขวาไม่มีใครสูงกว่า (รอดเลย)
        // หรือ ถ้ามันสูงกว่าต้นฝั่งขวา (h[i] > h[i+1]) ก็ถือว่ารอด
        if (i == n - 1 || h[i] > h[i+1]) {
            right_ok = 1;
        }

        // ถ้ารอดทั้งฝั่งซ้ายและฝั่งขวา แปลว่านกชอบทำรัง!
        if (left_ok == 1 && right_ok == 1) {
            count++;
        }
    }

    // 4. แสดงผลลัพธ์
    printf("%d\n", count);

    return 0;
}